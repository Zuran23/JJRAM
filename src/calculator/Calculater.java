package calculator;

public class Calculater {
    private  int a;
    private  int b;

    public Calculater() {
    }

    public Calculater(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}